package com.practice;

// Method Overloading
// this means that we can have
// the exact same method name, but with a different behavior
// and  set of parameters being passed into it.


public class SuperMath {

    // int data type (Primitive)
    // pass in  and add two integer parameters ( a, b )
    // return set value for variable a + b
    public int add( int a, int b) {
        return a + b;
    }

    // int data type (Primitive)
    // pass in  and add three integer parameters ( a, b, c )
    // return set value for variable a + b + c
    public int add( int a, int b, int c) {
        return a + b + c;
    }


}
